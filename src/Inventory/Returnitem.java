    private void txtidKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtidKeyPressed

        // TODO add your handling code here:
        if(evt.getKeyCode()==KeyEvent.VK_ENTER)
        {
        
       String id = txtid.getText();
        try {
            pst = con.prepareStatement("select m.name,b.iname,l.returndate,DATEDIFF(now(),l.returndate) as elap from issueitem l JOIN student m ON l.studentid = m.enno JOIN item b ON l.itemid=b.id and l.studentid = ?");
            pst.setString(1, id);
            rs = pst.executeQuery();
            if(rs.next()==false)
            {
                JOptionPane.showMessageDialog(this, "Membr ID not found");
            }
            else
            {
                String mname =rs.getString("m.name");
                String bname =rs.getString("b.iname");
                txtname.setText(mname.trim());
                txtitem.setText(bname.trim());
                String date = rs.getString("l.returndate");
                txtrdate.setText(date);
                String elp = rs.getString("elap");
                int elaped = Integer.parseInt(elp);
                if(elaped > 0)
                {
                    txtelp.setText(elp);
                    int fine = elaped * 10;
                    txtfine.setText(String.valueOf(fine));
                }
                else
                {
                    txtelp.setText("0");
                    txtfine.setText("0");
                    
                }
                
               
            }
            
            
            
            
            
            
                    
                    } catch (SQLException ex) {
            Logger.getLogger(Returnitem.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_txtidKeyPressed
    }
